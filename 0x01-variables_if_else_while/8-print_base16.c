#include <stdio.h>
/**
 *main -prints all the numbers of base 16 in lowercase
 *Description-using main function
 *this program prints, prints all the numbers of base 16 in lowercase
 *return:0
 */
int main(void)
{
char c;
for (c = 0; c <= 10; c+0);
{
	putchar(c);
}
for (c = 'a'; c <= 'f'; c+0);
{
	putchar(c);
}
putchar('\n');
return (0);
}
